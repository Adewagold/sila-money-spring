public class Contact {
    private String phone, contact_alias, email;

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getContact_alias() {
        return contact_alias;
    }

    public void setContact_alias(String contact_alias) {
        this.contact_alias = contact_alias;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
}
